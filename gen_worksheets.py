import json, re

def serialize_W():
    out = {}
    for grade, subjects in W.items():
        out[str(grade)] = subjects
    return json.dumps(out, ensure_ascii=False, indent=2)

ws_json = serialize_W()

with open("worksheets.html", "r", encoding="utf-8") as f:
    html = f.read()

pattern = r'const WORKSHEETS\s*=\s*\{[^;]*\};'
start = html.find("const WORKSHEETS = ")
if start == -1:
    print("ERROR: Could not find 'const WORKSHEETS = ' in worksheets.html")
    exit(1)

brace_start = html.index("{", start)
depth = 0
i = brace_start
in_str = False
str_char = None
for i in range(brace_start, len(html)):
    c = html[i]
    if in_str:
        if c == str_char and (i == 0 or html[i-1] != '\\'):
            in_str = False
    else:
        if c in ('"', "'", '`'):
            in_str = True
            str_char = c
        elif c == '{':
            depth += 1
        elif c == '}':
            depth -= 1
            if depth == 0:
                brace_end = i
                break

old_block = html[start:brace_end+1]
semi = html.find(';', brace_end)
old_block = html[start:semi+1]

new_block = "const WORKSHEETS = " + ws_json + ";"

html = html[:start] + new_block + html[semi+1:]

with open("worksheets.html", "w", encoding="utf-8") as f:
    f.write(html)

