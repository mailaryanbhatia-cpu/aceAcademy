def v(char, rom, meaning, cat, strokes=None, tips=""):
    s = f'null' if strokes is None else str(strokes)
    tips_escaped = tips.replace('\\','\\\\').replace('"','\\"').replace("'","\\'")
    return f'  {{char:"{char}",rom:"{rom}",meaning:"{meaning}",cat:"{cat}",strokes:{s},tips:"{tips_escaped}"}}'

