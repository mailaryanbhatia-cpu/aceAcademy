def find_matching_bracket(text, start):
    depth = 0
    i = start
    in_str = False
    sc = None
    while i < len(text):
        c = text[i]
        if in_str:
            if c == '\\': i += 2; continue
            if c == sc: in_str = False
        else:
            if c in ('"', "'", '`'): in_str = True; sc = c
            elif c == '[': depth += 1
            elif c == ']':
                depth -= 1
                if depth == 0: return i
        i += 1
    return -1

def patch_vocab(html, lang_marker, new_entries):
    # Find the language section
    lang_pos = html.find(lang_marker)
    if lang_pos == -1:
        print(f"ERROR: could not find {lang_marker}")
        return html
    # Find vocab:[ after the language marker
    vocab_pos = html.find("vocab:[", lang_pos)
    if vocab_pos == -1:
        vocab_pos = html.find("vocab: [", lang_pos)
        if vocab_pos == -1:
            print(f"ERROR: could not find vocab:[ for {lang_marker}")
            return html
        bracket_pos = vocab_pos + 7
    else:
        bracket_pos = vocab_pos + 6
    end_pos = find_matching_bracket(html, bracket_pos)
    if end_pos == -1:
        print(f"ERROR: could not find end of vocab array for {lang_marker}")
        return html
    new_vocab = "vocab:" + make_vocab_js(new_entries)
    html = html[:vocab_pos] + new_vocab + html[end_pos+1:]
    print(f"Patched {lang_marker}: {len(new_entries)} words")
    return html

